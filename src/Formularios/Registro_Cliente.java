    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
         modelotabla.setNumRows(0);

        Principales.Cliente objCliente = new Principales.Cliente();
        ResultSet resultado = objCliente.cargarCliente();
        try {
            Object data[] = new Object[5];
            while (resultado.next()) {
                for (int i = 0; i < 5; i++) {
                    data[i] = resultado.getObject(i + 1);
                }
                modelotabla.addRow(data);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error" + e.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        //Validar los campos vac{ios
   

        if (txtnombre.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese nombre");
            txtnombre.requestFocus();
            return;
        }
        if (txtapellido.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese apellido");
            txtapellido.requestFocus();
            return;
        }

        if (txttelefono.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Telefono");
            txttelefono.requestFocus();
            return;
        }

        if (txtdireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Ingrese Direccion");
            txtdireccion.requestFocus();
            return;


    }                                            
        //Instancio la clase 
        Principales.Cliente objTablaCliente = new Principales.Cliente();

        //Declaro variables
        int nit;
        String nombre;
        String apellido;
        int telefono;
        String direccion;

        //Asigno los valores del formualrio 
        nit = Integer.parseInt(txtnit.getText());
        nombre = txtnombre.getText();
        apellido = txtapellido.getText();
        telefono = Integer.parseInt(txttelefono.getText());
        direccion = txtdireccion.getText();

        boolean resultado = objTablaCliente.insertarCliente(nit,nombre, apellido, telefono, direccion);
        if (resultado == true) {
            JOptionPane.showMessageDialog(null, "Registro exitoso");
        } else {
            JOptionPane.showMessageDialog(null, "Registro no exitoso");

        }

        //Limpio los campos
        txtnit.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
        txttelefono.setText("");
        txtdireccion.setText("");

    }
